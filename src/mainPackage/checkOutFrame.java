    private void pesanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanButtonActionPerformed
        int hasil = 0;
        if (borobudurButton.isSelected()) {
            hasil = hitung(75000, (Integer) jmlTiketSpinner.getValue());
        } else if (kutaButton.isSelected()) {
            hasil = hitung(50000, (Integer) jmlTiketSpinner.getValue());
        } else if (bromoButton.isSelected()) {
            hasil = hitung(80000, (Integer) jmlTiketSpinner.getValue());
        } else if (tobaButton.isSelected()) {
            hasil = hitung(60000, (Integer) jmlTiketSpinner.getValue());
        } else {
            JOptionPane.showMessageDialog(this, "Mohon pilih satu wisata yang diinginkan.");
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Apakah Anda sudah yakin dengan pesanan Anda?", "", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            notaArea.setText("***************************************\n");
            notaArea.setText(notaArea.getText() + "*            Nota Pembelian Anda         *\n");
            notaArea.setText(notaArea.getText() + "***************************************\n");
            notaArea.setText(notaArea.getText() + "Total Tiket " + jmlTiketSpinner.getValue() + "\n\n");
            notaArea.setText(notaArea.getText() + "Total Harga\nRp" + hasil + " \n");
            notaArea.setText(notaArea.getText() + "***************************************\n");
            notaArea.setText(notaArea.getText() + "* Selamat Menikmati Liburan Anda! *\n");
        }
    }//GEN-LAST:event_pesanButtonActionPerformed

    private int hitung(int harga, int jmlTiket) {
        if (jmlTiket == 1) {
            return harga;
        }
        return harga + hitung(harga, jmlTiket - 1);
    }

