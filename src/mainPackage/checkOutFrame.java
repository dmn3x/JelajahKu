    private void sortPopulerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortPopulerActionPerformed
        String[] nama = {"Candi Borobudur", "Pantai Kuta", "Gunung Bromo", "Danau Toba"};
        int[] harga = {75000, 50000, 80000, 60000};
        int[] popularitas = {210, 1730, 4500, 390};
        JRadioButton[] radioButtons = {borobudurButton, kutaButton, bromoButton, tobaButton};

        for (int i = 0; i < popularitas.length - 1; i++) {
            for (int j = 0; j < popularitas.length - i - 1; j++) {
                if (popularitas[j] < popularitas[j + 1]) {

                    int tempPopularitas = popularitas[j];
                    popularitas[j] = popularitas[j + 1];
                    popularitas[j + 1] = tempPopularitas;

                    String tempNama = nama[j];
                    nama[j] = nama[j + 1];
                    nama[j + 1] = tempNama;

                    int tempHarga = harga[j];
                    harga[j] = harga[j + 1];
                    harga[j + 1] = tempHarga;
                }
            }
        }
        for (int i = 0; i < radioButtons.length; i++) {
            radioButtons[i].setText(nama[i] + "     " + popularitas[i] + " pengunjung");
        }
    }//GEN-LAST:event_sortPopulerActionPerformed

